package XudongYu;

public class Triangile {

    private int age1;
    private int age2;
    private int age3;

    public Triangile(int age1, int age2, int age3) {
        this.age1 = age1;
        this.age2 = age2;
        this.age3 = age3;
    }

    public int getAge1() {
        return this.age1;
    }

    public int getAge2() {
        return this.age2;
    }

    public int getAge3() {
        return this.age3;
    }
}
